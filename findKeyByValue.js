const findKeyByValue = function(myObject, value) {
  let myArray = Object.entries(myObject);
  for (let i = 0; i < myArray.length; i++) {
    if (myArray[i][1] === value) {
      return myArray[i][0];
    }
  }
  return undefined;
};

